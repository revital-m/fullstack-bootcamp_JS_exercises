function describeCountry(country, population, capitalCity){
    return `${country} has ${population} million people and its capital city is ${capitalCity}`;
};

let var1 = describeCountry('Israel', 9, 'Jerusalem');
let var2 = describeCountry('Spain', 46, 'Madrid');
let var3 = describeCountry('France', 65, 'Paris');

console.log(var1);
console.log(var2);
console.log(var3);